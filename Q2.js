// Function to generate personalized greetings
function generateGreeting(name) {
    return `Hello, ${name}! Welcome to our website.`;
}

// Example usage
const person1 = "Alice";
const person2 = "Bob";
const person3 = "Charlie";

console.log(generateGreeting(person1)); // Outputs: Hello, Alice! Welcome to our website.
console.log(generateGreeting(person2)); // Outputs: Hello, Bob! Welcome to our website.
console.log(generateGreeting(person3)); // Outputs: Hello, Charlie! Welcome to our website.
